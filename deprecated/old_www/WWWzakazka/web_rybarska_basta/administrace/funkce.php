function velikost_adresare($jmeno,$koncovka)
{
  $handle = opendir($jmeno);
  $vel = 0;
  while($soub = readdir($handle))
  {
  $vel += filesize("$jmeno/$soub");
  }
  closedir($handle);

  if($koncovka == "true")
  {
    if($vel >= 1048576)
    {
      return sprintf("%.2lf&nbsp;MB", round($vel / 1048576 * 100) / 100);
    }
      else
    if($vel >= 1024)
    {
      return sprintf("%.2lf&nbsp;kB", round($vel / 1024 * 100) / 100);
    }
      else
    {
      return sprintf("%.2lf&nbsp;Bytes", $vel);
    }
  }
    else //else koncovka
  {
    return $vel;
  }
}
